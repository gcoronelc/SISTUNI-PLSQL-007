CREATE OR REPLACE PROCEDURE SP_GET_SALDO_TOTAL_EMP
(V_ID IN CHAR,V_SUELDO OUT NUMBER)
AS
 SUELDO NUMBER;
 COIMISION NUMBER;
BEGIN
SELECT SUM(E.SUELDO) INTO SUELDO FROM EMPLEADO E
WHERE E.IDEMPLEADO=V_ID;

SELECT SUM(E.COMISION) INTO COIMISION FROM EMPLEADO E
WHERE E.IDEMPLEADO=V_ID;

V_SUELDO:=SUELDO + COIMISION;

END;



DECLARE V_SAL NUMBER;
BEGIN
SP_GET_SALDO_TOTAL_EMP('E0005',V_SAL);

DBMS_OUTPUT.PUT_LINE('SUELDO DEL TRABAJADOR2: ' || V_SAL);
END;