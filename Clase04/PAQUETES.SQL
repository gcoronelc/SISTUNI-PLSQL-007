
-- DEFINICION DEL PAQUETE: PKG_DEMO01

CREATE OR REPLACE PACKAGE SCOTT.PKG_DEMO01 AS


  FUNCTION FN_PROMEDIO( P_N1 NUMBER, P_N2 NUMBER )
  RETURN NUMBER;
  
  FUNCTION FN_PROMEDIO( P_N1 NUMBER, P_N2 NUMBER, P_N3 NUMBER )
  RETURN NUMBER;


END PKG_DEMO01;



-- IMPLEMENTACION DEL PAQUETE: PKG_DEMO01

CREATE OR REPLACE PACKAGE BODY SCOTT.PKG_DEMO01 AS


  FUNCTION FN_PROMEDIO( P_N1 NUMBER, P_N2 NUMBER )
  RETURN NUMBER
  IS
    V_SUMA NUMBER;
  BEGIN
    V_SUMA := P_N1 + P_N2;
    RETURN ( V_SUMA / 2 );
  END;

  FUNCTION FN_PROMEDIO( P_N1 NUMBER, P_N2 NUMBER, P_N3 NUMBER )
  RETURN NUMBER
  IS
    V_SUMA NUMBER;
  BEGIN
    V_SUMA := P_N1 + P_N2 + P_N3;
    RETURN ( V_SUMA / 3 );
  END;
  
END PKG_DEMO01;



SELECT 
  PKG_DEMO01.FN_PROMEDIO( 12, 15 ) PROM1,
  PKG_DEMO01.FN_PROMEDIO( 12, 15, 18 ) PROM2
FROM DUAL;
