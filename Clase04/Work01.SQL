IF ( P_IDP_GOBIERNO IS NULL AND P_APE_PATERNO IS NULL AND P_APE_MATERNO IS NULL 
       AND P_NOMBRE IS NULL AND P_DNI IS NULL ) THEN
		V_CONTROL := 1;
	END IF;

	V_APE_PATERNO  := '%' || UPPER( TRIM( P_APE_PATERNO ) ) || '%';
	V_APE_MATERNO  := '%' || UPPER( TRIM( P_APE_MATERNO ) ) || '%';
	V_NOMBRE       := '%' || UPPER ( TRIM ( P_NOMBRE ) ) || '%';
  V_DNI          := '%' || P_DNI || '%';

	OPEN p_cursor FOR
  WITH 
  V_GOB AS (
    SELECT ID_PARAMETRO CODIGO, DESCRIPCION GOBIERNO
    FROM SISCE.T_SCEL_PARAMETROS
    WHERE ID_PARAMETRO LIKE '34%'
    AND LENGTH(ID_PARAMETRO) = 5
  )
	SELECT 
    ID_PROFESIONAL ID, USUARIO, DNI, APELLIDO_PATERNO PATERNO, 
    APELLIDO_MATERNO MATERNO, NOMBRES, V_GOB.GOBIERNO GOBIERNO, 
    CORREO, CASE WHEN ESTADO = '1' THEN 'ACTIVO' ELSE 'INACTIVO' END ESTADO 
	FROM SISCE.T_SCEM_PROFESIONALES PROF
  LEFT JOIN V_GOB ON PROF.IDP_GOBIERNO = V_GOB.CODIGO
	WHERE UPPER( NVL(PROF.APELLIDO_PATERNO,'X') ) LIKE CASE WHEN P_APE_PATERNO IS NULL THEN UPPER( NVL(PROF.APELLIDO_PATERNO,'X') ) ELSE V_APE_PATERNO END    
	AND   UPPER( NVL(PROF.APELLIDO_MATERNO,'X' )) LIKE CASE WHEN P_APE_MATERNO IS NULL THEN UPPER( NVL(PROF.APELLIDO_MATERNO,'X' )) ELSE V_APE_MATERNO END   
	AND   UPPER( NVL(PROF.NOMBRES,'X') )          LIKE CASE WHEN P_NOMBRE      IS NULL THEN UPPER( NVL(PROF.NOMBRES,'X') ) ELSE V_NOMBRE END
  AND   NVL(PROF.DNI,'X')                       LIKE CASE WHEN P_DNI         IS NULL THEN NVL(PROF.DNI,'X')              ELSE V_DNI END
  AND   NVL(PROF.IDP_GOBIERNO,'X')              = CASE WHEN V_IDP_GOBIERNO IS NULL THEN NVL(PROF.IDP_GOBIERNO,'X') ELSE V_IDP_GOBIERNO END
  -- AND   ESTADO='1'
	AND   ROWNUM <= CASE WHEN V_CONTROL = 1 THEN 50 ELSE ROWNUM END;