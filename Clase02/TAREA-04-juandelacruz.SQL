CREATE OR REPLACE FUNCTION FN_NOTA_CONDICION
(P_NOTA NUMBER) 
RETURN VARCHAR2
AS
	V_CONDICION VARCHAR2(200);
BEGIN	
	
	CASE
		WHEN (P_NOTA >= 14 AND P_NOTA <= 20) THEN 
			V_CONDICION := 'APROBADO';
		WHEN (P_NOTA >= 10 AND P_NOTA < 14) THEN 
			V_CONDICION := 'ASISTENTE';
		WHEN P_NOTA < 10 THEN 
			V_CONDICION := 'DESAPROBADO';
		ELSE 
			V_CONDICION :='FUERA DE RANGO';
	END CASE;
	RETURN V_CONDICION;
END;
/

DECLARE
V_CONDICION VARCHAR2(200);
BEGIN
V_CONDICION := FN_NOTA_CONDICION(-21);
DBMS_OUTPUT.PUT_LINE('CONDICION: ' || V_CONDICION);
END;
