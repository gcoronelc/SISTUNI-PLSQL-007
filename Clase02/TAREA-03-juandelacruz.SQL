CREATE OR REPLACE FUNCTION FN_PROMEDIO_V2
(P_N1 NUMBER, P_N2 NUMBER, P_N3 NUMBER, P_N4 NUMBER)
RETURN NUMBER AS
	V_PROMEDIO NUMBER;
	V_MNOTA NUMBER;
BEGIN
	
	
	IF(P_N1 < P_N2) THEN
		V_MNOTA := P_N1;
	ELSE
		V_MNOTA := P_N2;
	END IF;
	
	IF(P_N3<V_MNOTA) THEN
		V_MNOTA := P_N2;
	END IF;
	
	IF(P_N4<V_MNOTA) THEN
		V_MNOTA := P_N4;
	END IF;
	
	V_PROMEDIO := ((P_N1 + P_N2+ P_N3+ P_N4)-V_MNOTA)/3;
	RETURN V_PROMEDIO;
END;

DECLARE
V_PROM NUMBER;
BEGIN
V_PROM := FN_PROMEDIO_V2(10,10,10,5);
DBMS_OUTPUT.PUT_LINE('PROMEDIO: ' || V_PROM);
END;