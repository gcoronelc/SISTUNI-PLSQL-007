CREATE OR REPLACE PROCEDURE SCOTT.IMPORTE_VENTA
(
  PRECIO   IN  NUMBER,
  CANTIDAD IN  NUMBER,
  IMPORTE  OUT NUMBER,
  IMPUESTO OUT NUMBER,
  TOTAL    OUT NUMBER
)

AS

BEGIN
 
  IMPORTE :=  PRECIO * CANTIDAD;
  IMPUESTO := IMPORTE * 0.18;
  TOTAL:= IMPORTE + IMPUESTO;
 
  dbms_output.PUT_LINE('IMPORTE: ' || IMPORTE);
  dbms_output.PUT_LINE('IMPUESTO: ' || IMPUESTO);
  dbms_output.PUT_LINE('TOTAL: ' || TOTAL);

END;

DECLARE 
  P_IMPORTE number;
  P_IMPUESTO number;
  P_TOTAL number;
BEGIN
  SCOTT.IMPORTE_VENTA (50, 2, P_IMPORTE, P_IMPUESTO, P_TOTAL);
  DBMS_OUTPUT.PUT_LINE('IMPORTE: ' || P_IMPORTE);
  DBMS_OUTPUT.PUT_LINE('IMPUESTO: ' || P_IMPUESTO);
  DBMS_OUTPUT.PUT_LINE('TOTAL: ' || P_TOTAL);
END;
