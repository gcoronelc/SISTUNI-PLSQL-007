CREATE OR REPLACE FUNCTION SCOTT.ES_PAR_V1
( P_NUM NUMBER ) 
RETURN VARCHAR2
AS
  V_RPTA VARCHAR2(30);
BEGIN
  V_RPTA := 'SI ES PAR';
  IF MOD( P_NUM, 2 ) = 1 THEN
    V_RPTA := 'NO ES PAR';
  END IF;
  RETURN V_RPTA;
END;
/


SELECT SCOTT.ES_PAR_V1(11) FROM DUAL;


