CREATE OR REPLACE FUNCTION SCOTT.FN_MCD
(
  P_N1 NUMBER,
  P_N2 NUMBER
)
RETURN VARCHAR2 
AS
 V_MCD   VARCHAR2(200);
 V_MENOR NUMBER;
BEGIN

	V_MCD := 0;
	V_MENOR := P_N1;
	
	IF (P_N2 < V_MENOR) THEN
		V_MENOR := P_N2;
	END IF;
	
	FOR N IN 1 .. V_MENOR LOOP
		IF (MOD(P_N1,N)=0 AND  MOD(P_N2,N)=0) THEN
			V_MCD := N;
		END IF;
	END LOOP;
	
	RETURN V_MCD;
	
END;


SELECT SCOTT.FN_MCD(5,7) FROM DUAL;

