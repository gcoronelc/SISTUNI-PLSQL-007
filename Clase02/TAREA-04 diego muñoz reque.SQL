CREATE OR REPLACE FUNCTION CALIFICACION_AL(P_NUM NUMBER)
RETURN VARCHAR2
AS
  CALIF VARCHAR2(100);
BEGIN
  CASE 
  WHEN P_NUM>=14 THEN CALIF := 'APROBADO';
  WHEN (P_NUM>=10 AND P_NUM <14) THEN CALIF := 'ASISTENTE';
  WHEN  P_NUM < 10 THEN CALIF := 'DESAPROBADO';
  ELSE CALIF:= 'INVALIDO';
  END CASE;
RETURN CALIF;
END;
/


SELECT CALIFICACION_AL(11) FROM DUAL;




