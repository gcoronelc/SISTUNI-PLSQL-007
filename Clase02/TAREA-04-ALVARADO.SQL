CREATE OR REPLACE FUNCTION SCOTT.FN_ALUMNO
(V_NOTA NUMBER)
RETURN VARCHAR2

AS
S_NOTA VARCHAR2(200);
BEGIN

CASE 
WHEN (V_NOTA>=14 AND V_NOTA<=20) THEN S_NOTA := 'APROBADO';
WHEN (V_NOTA>=10 AND V_NOTA<14) THEN S_NOTA := 'ASISTENTE';
WHEN V_NOTA<10 AND V_NOTA>=0  THEN S_NOTA := 'DESAPROBADO';

ELSE S_NOTA := 'NOTA FUERA DEL RANGO DE EVALUACION';
END CASE;
RETURN S_NOTA;
END;

