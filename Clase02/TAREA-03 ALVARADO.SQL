
CREATE OR REPLACE FUNCTION SCOTT.FN_CONDICION
(P_N1 NUMBER, P_N2 NUMBER, P_N3 NUMBER, P_N4 NUMBER)
RETURN NUMBER 
AS
	V_PROMEDIO NUMBER;
	V_MENOR  NUMBER;
BEGIN


IF P_N1<P_N2 THEN
V_MENOR := P_N1;
ELSE
V_MENOR := P_N2;
END IF;
-- P_N3
IF P_N3<V_MENOR THEN
V_MENOR := P_N3;
END IF;
-- P_N4
IF P_N4<V_MENOR THEN
V_MENOR := P_N4;
END IF;

	V_PROMEDIO := ((P_N1 + P_N2+ P_N3+ P_N4)-V_MENOR)/3;
		
	
	RETURN V_PROMEDIO;
END;

