CREATE OR REPLACE FUNCTION FN_CALCULAR_SUMA
(P_PA NUMBER, P_PB NUMBER) 
RETURN NUMBER
AS
	V_SUMA NUMBER;
BEGIN	
	V_SUMA := 0;
	FOR N IN P_PA .. P_PB LOOP
		V_SUMA := V_SUMA + N;
	END LOOP;
	
	RETURN V_SUMA;
END;
/

DECLARE
  V_RESULT NUMBER;
BEGIN
  V_RESULT := FN_CALCULAR_SUMA(1, 3);
  DBMS_OUTPUT.PUT_LINE('RSULTADO: ' || V_RESULT);
END;


